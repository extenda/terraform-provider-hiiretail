# 2. Resource documentation with correct naming
RESOURCES=("iam_custom_role" "iam_group" "iam_resource" "iam_role_binding")
for resource in "${RESOURCES[@]}"; do
    if [ ! -f "docs/resources/${resource}.md" ]; then
        echo "âŒ Missing docs/resources/${resource}.md"
        exit 1
    fi
    
    # Check that it doesn't have provider prefix
    if [ -f "docs/resources/hiiretail_${resource}.md" ]; then
        echo "âŒ Found incorrect file docs/resources/hiiretail_${resource}.md (should not have provider prefix)"
        exit 1
    fi
done
echo "âœ… All resource docs use correct naming (no provider prefix)"

# 3. Check for required sections in provider index
if ! grep -q "## Example Usage" docs/index.md; then
    echo "âŒ docs/index.md missing required 'Example Usage' section"
    exit 1
fi

if ! grep -q "## Schema\|## Argument Reference" docs/index.md; then
    echo "âŒ docs/index.md missing required 'Argument Reference' or 'Schema' section"
    exit 1
fi
echo "âœ… Provider index has required sections"

# 4. Check examples directory structure
